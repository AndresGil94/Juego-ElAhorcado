package model;

public class Words {
	
	private int id;
	private String word;
	
	public Words() {
		super();
	}

	public Words(String word) {
		this.word = word;
	}

	public String getWord() {
		return word;
	}

	public void setWord(String word) {
		this.word = word;
	}

	public int getId() {
		return id;
	}
	 
	

	
}
